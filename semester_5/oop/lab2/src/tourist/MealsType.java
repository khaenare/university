package tourist;

/**
 * Тип харчування в готелі.
 * Значення відповідають стандартним скороченням:
 * RO, BB, HB, FB, AI.
 */
public enum MealsType {
    RO,  // Без харчування (Room Only)
    BB,  // Сніданок (Bed & Breakfast)
    HB,  // Напівпансіон (Half Board)
    FB,  // Повний пансіон (Full Board)
    AI   // Все включено (All Inclusive)
}
